create table users
(
    id              bigint generated by default as identity,
    email           varchar(50),
    first_name      varchar(50),
    last_name       varchar(50),
    password        varchar(50),
    phone           varchar(50),
    role            varchar(50),
    login_email varchar(50),
    user_image_url  varchar(50),
    primary key (id),
    foreign key (user_image_url) references image(url)
);